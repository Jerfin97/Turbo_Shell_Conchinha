#include "lib_mini.h"

void	ft_printenv(t_shell *blk)
{
	int	i;

	i = 0;
	while (blk->envp[i])
	{
		printf("%s\n", blk->envp[i]);
		i++;
	}
	blk->rs = 0;
}
